    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = $this->userRepository->find($id);
        if(Gate::denies('destroy', $user)){
            return redirect()->route('admin.user.index')->with('status','Не хватает прав доступа для удаления');
        }
        $name = $user->name;
        $user->roles()->detach();
        $user->delete();
        return redirect()->route('admin.user.index')->with('status','Пользователь '.$name.' удален');
    }
